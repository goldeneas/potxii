# MicroPython SSD1306 OLED driver, I2C and SPI interfaces
from micropython import const
import framebuf
import utime

# register definitions
SET_CONTRAST = const(0x81)
SET_ENTIRE_ON = const(0xA4)
SET_NORM_INV = const(0xA6)
SET_DISP = const(0xAE)
SET_MEM_ADDR = const(0x20)
SET_COL_ADDR = const(0x21)
SET_PAGE_ADDR = const(0x22)
SET_DISP_START_LINE = const(0x40)
SET_SEG_REMAP = const(0xA0)
SET_MUX_RATIO = const(0xA8)
SET_COM_OUT_DIR = const(0xC0)
SET_DISP_OFFSET = const(0xD3)
SET_COM_PIN_CFG = const(0xDA)
SET_DISP_CLK_DIV = const(0xD5)
SET_PRECHARGE = const(0xD9)
SET_VCOM_DESEL = const(0xDB)
SET_CHARGE_PUMP = const(0x8D)

# Subclassing FrameBuffer provides support for graphics primitives
# http://docs.micropython.org/en/latest/pyboard/library/framebuf.html
class SSD1306(framebuf.FrameBuffer):
    def __init__(self, width, height, external_vcc):
        self.width = width
        self.height = height
        self.external_vcc = external_vcc
        self.pages = self.height // 8
        self.buffer = bytearray(self.pages * self.width)
        super().__init__(self.buffer, self.width, self.height, framebuf.MONO_VLSB)
        self.init_display()

    def init_display(self):
        for cmd in (
            SET_DISP | 0x00,  # off
            # address setting
            SET_MEM_ADDR,
            0x00,  # horizontal
            # resolution and layout
            SET_DISP_START_LINE | 0x00,
            SET_SEG_REMAP | 0x01,  # column addr 127 mapped to SEG0
            SET_MUX_RATIO,
            self.height - 1,
            SET_COM_OUT_DIR | 0x08,  # scan from COM[N] to COM0
            SET_DISP_OFFSET,
            0x00,
            SET_COM_PIN_CFG,
            0x02 if self.width > 2 * self.height else 0x12,
            # timing and driving scheme
            SET_DISP_CLK_DIV,
            0x80,
            SET_PRECHARGE,
            0x22 if self.external_vcc else 0xF1,
            SET_VCOM_DESEL,
            0x30,  # 0.83*Vcc
            # display
            SET_CONTRAST,
            0xFF,  # maximum
            SET_ENTIRE_ON,  # output follows RAM contents
            SET_NORM_INV,  # not inverted
            # charge pump
            SET_CHARGE_PUMP,
            0x10 if self.external_vcc else 0x14,
            SET_DISP | 0x01,
        ):  # on
            self.write_cmd(cmd)
        self.fill(0)
        self.show()

    def poweroff(self):
        self.write_cmd(SET_DISP | 0x00)

    def poweron(self):
        self.write_cmd(SET_DISP | 0x01)

    def contrast(self, contrast):
        self.write_cmd(SET_CONTRAST)
        self.write_cmd(contrast)

    def invert(self, invert):
        self.write_cmd(SET_NORM_INV | (invert & 1))

    #Questa è la funzione che prende il buffer grafico di FrameBuffer
    #e lo invia davvero al display via I2C/SPI.
    def show(self):
        #imposto i limiti delle colonne da 0 a 127 (display 128 pixel)
        x0 = 0
        x1 = self.width - 1
        if self.width == 64:
            # displays with width of 64 pixels are shifted by 32
            x0 += 32
            x1 += 32
        #Imposta l’intervallo delle colonne [0,127]
        self.write_cmd(SET_COL_ADDR)
        self.write_cmd(x0)
        self.write_cmd(x1)
        
        #Imposta l’intervallo delle pagine
        #Ogni pagina è alta 8 pixel. Per un 128×64 ci sono: 64/8=8 pagine
        # quindi scrivi l’intero schermo dall’alto al basso da pagina 0 a 7
        
        self.write_cmd(SET_PAGE_ADDR)
        self.write_cmd(0)
        self.write_cmd(self.pages - 1)
        self.write_data(self.buffer)

    def draw_image(self, buffer, w, h, x, y):
        #Crea un oggetto FrameBuffer, una struttura dati che contiene i pixel di una immagine:
        #buffer → i dati binari dell’immagine,
        #128, 64 → larghezza e altezza in pixel,
        #framebuf.MONO_HLSB → tipo di formato monocromatico usato dal display
        #(ogni bit rappresenta un pixel, “Horizontal Least Significant Bit”).
        #MONO → immagine monocromatica (bianco/nero)
        #H → Horizontal: i bit scorrono orizzontalmente lungo le righe)
        #LSB → Least Significant Bit first: da sin a destra 
        fb = framebuf.FrameBuffer(buffer, w, h, framebuf.MONO_HLSB)
        self.fill(0) #pulisce lo schermo
        #Copia (o “incolla”) l’immagine (fb) sul display a partire dalla posizione
        
        self.blit(fb, x, y) #centra l'immagine sia in verticale che in orizzontale 
        self.show()
        utime.sleep_ms(100)

